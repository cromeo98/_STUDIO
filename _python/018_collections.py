# ogni collezione ha delle proprie proprietà:
# proprietà delle collezioni:
# ordinato: la collezione ha un ordine ben definito e l'aggiunta di altri elementi non incide
# indicizzato: posso accedere ai dati della collezione tramite un indice
# modificabile: è possibile aggiungere, cambiare e rimuovere elementi dalla collezione
# immutabile: è una collezione in cui non possiamo aggiungere, cambiare o rimuovere elementi
# contiene duplicti: sono le collezioni che permettono di avere lo stesso valore due volte

# # lista (collezione), tra parentesi quadre - non confondere con gli array
# # sono:
