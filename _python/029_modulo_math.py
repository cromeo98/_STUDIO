# math è un modulo built in di python che permette di eseguire delle operazioni matematiche avanzate e complesse
# in python di base possiamo svolgere operazioni aritmetiche di base
# ci sono anche delle funzioni matematiche built-in (min, max, abs, pow)
# con math, le opzioni sono:

# Costanti
# math.pi: Il valore della costante π (pi greco).
# math.e: Il valore della costante e (numero di Eulero, base dei logaritmi naturali).
# math.tau: Il valore della costante τ (tau), uguale a 2π.
# math.inf: Rappresenta l'infinito positivo.
# math.nan: Rappresenta un valore "Not a Number" (NaN).

# Funzioni aritmetiche di base
# math.ceil(x): Restituisce l'intero più piccolo maggiore o uguale a x.
# math.floor(x): Restituisce l'intero più grande minore o uguale a x.
# math.trunc(x): Tronca un numero x verso lo zero, eliminando la parte decimale.
# math.fabs(x): Restituisce il valore assoluto di x come numero in virgola mobile.
# math.factorial(x): Restituisce il fattoriale di x (solo per interi).
# math.fmod(x, y): Restituisce il resto della divisione di x per y.
# math.remainder(x, y): Restituisce il resto di x diviso per y, seguendo le regole di arrotondamento più vicino.
# math.gcd(x, y): Restituisce il massimo comun divisore (MCD) tra x e y.
# math.isfinite(x): Verifica se x è un numero finito (né infinito né NaN).
# math.isinf(x): Verifica se x è infinito.
# math.isnan(x): Verifica se x è un numero NaN (Not a Number).
# math.copysign(x, y): Restituisce x con il segno di y.

# Potenze e logaritmi
# math.exp(x): Restituisce e elevato alla potenza di x.
# math.expm1(x): Restituisce e^x - 1 (utile per piccoli valori di x).
# math.log(x[, base]): Restituisce il logaritmo naturale di x, oppure il logaritmo in base base se specificato.
# math.log1p(x): Restituisce il logaritmo naturale di 1 + x.
# math.log2(x): Restituisce il logaritmo in base 2 di x.
# math.log10(x): Restituisce il logaritmo in base 10 di x.
# math.pow(x, y): Restituisce x elevato alla potenza y.
# math.sqrt(x): Restituisce la radice quadrata di x.

# Funzioni trigonometriche
# math.sin(x): Restituisce il seno di x (in radianti).
# math.cos(x): Restituisce il coseno di x (in radianti).
# math.tan(x): Restituisce la tangente di x (in radianti).
# math.asin(x): Restituisce l'arcotangente di x.
# math.acos(x): Restituisce l'arcocoseno di x.
# math.atan(x): Restituisce l'arcotangente di x.
# # math.atan2(y, x): Restituisce l'arcotangente del quoziente y/x considerando il segno dei due argomenti per determinare il quadrante corretto.
# math.hypot(x, y): Restituisce la lunghezza dell'ipotenusa di un triangolo rettangolo con cateti x e y.

# Funzioni trigonometriche iperboliche
# math.sinh(x): Restituisce il seno iperbolico di x.
# math.cosh(x): Restituisce il coseno iperbolico di x.
# math.tanh(x): Restituisce la tangente iperbolica di x.
# math.asinh(x): Restituisce l'arcosenoh iperbolico di x.
# math.acosh(x): Restituisce l'arcocoseno iperbolico di x.
# math.atanh(x): Restituisce l'arcotangente iperbolica di x.

# Funzioni angolari
# math.degrees(x): Converte un angolo x da radianti a gradi.
# math.radians(x): Converte un angolo x da gradi a radianti.

# Funzioni speciali
# math.erf(x): Restituisce la funzione di errore di x.
# math.erfc(x): Restituisce la funzione di errore complementare di x.
# math.gamma(x): Restituisce la funzione gamma di x.
# math.lgamma(x): Restituisce il logaritmo naturale del valore assoluto della funzione gamma di x.

# Funzioni di combinatoria
# math.comb(n, k): Restituisce il numero di combinazioni di n oggetti presi k alla volta.
# math.perm(n, k): Restituisce il numero di permutazioni di n oggetti presi k alla volta.